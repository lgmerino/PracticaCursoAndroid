package com.example.cargaterminal;

public class ObjectVoyage{
	
	private String mVoyage;
	private String mDate;
	private Integer mUnitsToLoad;
	private Integer mLoadingLeg;
	
	public String getmVoyage() {
		return mVoyage;
	}
	public void setmVoyage(String mVoyage) {
		this.mVoyage = mVoyage;
	}
	public String getmDate() {
		return mDate;
	}
	public void setmDate(String mDate) {
		this.mDate = mDate;
	}
	public Integer getmUnitsToLoad() {
		return mUnitsToLoad;
	}
	public void setmUnitsToLoad(Integer mUnitsToLoad) {
		this.mUnitsToLoad = mUnitsToLoad;
	}
	public Integer getmLoadingLeg() {
		return mLoadingLeg;
	}
	public void setmLoadingLeg(Integer mLoadingLeg) {
		this.mLoadingLeg = mLoadingLeg;
	}

	
	
}